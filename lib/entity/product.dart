class Product {
  final int id;
  final String name;
  final int serving;
  final int protein;
  final int fat;
  final int carbohydrates;
  final int energyValue;
  final String categories;

  Product({
    required this.id,
    required this.name,
    required this.serving,
    required this.protein,
    required this.fat,
    required this.carbohydrates,
    required this.energyValue,
    required this.categories,
  });
}
